# FUNCION range([ini], fin, [inc])
print(list(range(5))) # -> 0, 1, 2, 3, 4
print(list(range(19))) # 0, .., 18

print(list(range(2, 10))) # 2, 3, 4, .., 9
print(list(range(15, -3))) # 15, .., -2

print(list(range(2, 10, 3))) # 2, 5, 8
print(list(range(15, -3, -1))) # 15, .., -

print(list(range(1,5,3))) # 1, 4 